package MODEL;
public class Interestmodel {
 
    private String ac_no, ac_name, ac_phone, balance, crtdate, typacc, timep, intrate;

    // Constructor
    public Interestmodel() {
    }

    // Getters and Setters
    public String getAc_no() {
        return ac_no;
    }

    public void setAc_no(String ac_no) {
        this.ac_no = ac_no;
    }

    public String getAc_name() {
        return ac_name;
    }

    public void setAc_name(String ac_name) {
        this.ac_name = ac_name;
    }

    public String getAc_phone() {
        return ac_phone;
    }

    public void setAc_phone(String ac_phone) {
        this.ac_phone = ac_phone;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getCrtdate() {
        return crtdate;
    }

    public void setCrtdate(String crtdate) {
        this.crtdate = crtdate;
    }

    public String getTypacc() {
        return typacc;
    }

    public void setTypacc(String typacc) {
        this.typacc = typacc;
    }

    public String getTimep() {
        return timep;
    }

    public void setTimep(String timep) {
        this.timep = timep;
    }

    public String getIntrate() {
        return intrate;
    }

    public void setIntrate(String intrate) {
        this.intrate = intrate;
    }
}
